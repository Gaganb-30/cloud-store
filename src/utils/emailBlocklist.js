/**
 * Check if an email domain is disposable/temporary
 * @param {string} email - Email address to check
 * @returns {boolean} - True if the domain is blocked
 */
export function isDisposableEmail(email) {
    if (!email || typeof email !== 'string') return false;

    const domain = email.toLowerCase().split('@')[1];
    if (!domain) return false;

    return disposableEmailDomains.has(domain);
}

/**
 * Get list of blocked domains (for reference)
 */
export function getBlockedDomains() {
    return Array.from(disposableEmailDomains);
}

export default { isDisposableEmail, getBlockedDomains };
